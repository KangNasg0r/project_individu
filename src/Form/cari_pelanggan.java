    private void bpilihActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bpilihActionPerformed
        if (formTransaksi != null) {
            // Ambil data dari text field di form cari_pelanggan
            String id = id_pel.getText();
            String nama = nama_pel.getText();
            String alamat = alamat_pel.getText();

            // Set data ke text field di form transaksi_pembayaran
            formTransaksi.id_pel.setText(id);
            formTransaksi.nama_pel.setText(nama);
            formTransaksi.alamat_pel.setText(alamat);

            // Tutup form cari_pelanggan setelah data dipilih
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(this, "Form transaksi pembayaran tidak ditemukan!");
            this.dispose();
        }
    }//GEN-LAST:event_bpilihActionPerformed

